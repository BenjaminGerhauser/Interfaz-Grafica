    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        DAOBiblioteca DAO = new DAOBiblioteca();
        String titulo = txtTitulo.getText();
        String autor = txtAutor.getText();
        int anioPublicacion = Integer.parseInt(txtAnioPublicacion.getText());
        int ISBN = Integer.parseInt(txtISBN.getText());
        boolean disponible;
        disponible = chkDisponible.isSelected();
        
        modeloLibro libro = new modeloLibro(titulo,autor,anioPublicacion,ISBN,disponible);

        int idLibro = DAO.agregarLibro(libro);
        libro.setId(idLibro);
        JOptionPane.showMessageDialog(null, "Libro Agregado");
    }//GEN-LAST:event_btnAgregarActionPerformed

