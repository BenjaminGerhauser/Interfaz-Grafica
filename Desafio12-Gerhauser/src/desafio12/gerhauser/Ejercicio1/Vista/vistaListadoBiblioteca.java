    private void cboOpcionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboOpcionActionPerformed

        if (cboOpcion.getSelectedItem() == "Todos") {
            lblAutor.setVisible(false);
            txtAutor.setVisible(false);
            btnBuscar.setVisible(false);
            listaLibros = DAO.listarLibros();
            listarLibros();
        } else if (cboOpcion.getSelectedItem() == "Autor") {
            lblAutor.setVisible(true);
            txtAutor.setVisible(true);
            btnBuscar.setVisible(true);
            DefaultTableModel model = (DefaultTableModel) tableListado.getModel();
            model.setRowCount(0);
        } else {
            lblAutor.setVisible(false);
            txtAutor.setVisible(false);
            btnBuscar.setVisible(false);
            listaLibros = DAO.listarDisponibles();
            listarLibros();
        }
    }//GEN-LAST:event_cboOpcionActionPerformed

